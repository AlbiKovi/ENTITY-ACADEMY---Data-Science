# computing a Z-Score

# In the Basic Statistics module I was informed that in Ghana, 
# the height of a young adult woman is normally distributed with a mean of 
# 159.0 cm and a standard deviation of about 4.9 cm.
# 
# Gabianu is a college student originally from Ghana, and she stands 169.0 cm 
# tall. I can compute the z-score for Gabianu's height.

x = 169.0
y = 154.8
mu = 159.0
sigma = 4.9

(x - mu) / sigma
(y - mu) / sigma


